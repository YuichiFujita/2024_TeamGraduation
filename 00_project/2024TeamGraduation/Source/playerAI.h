#ifndef _PLAYER_AI_
#define _PLAYER_AI_		// 二重インクルード防止

//==========================================================================
// インクルードファイル
//==========================================================================
#include "player.h"

//==========================================================================
// クラス定義
//==========================================================================
// AIプレイヤークラス
class CPlayerAI : public CPlayer
{
public:
	
	//=============================
	// コンストラクタ/デストラクタ
	//=============================
	CPlayerAI();
	~CPlayerAI();

	//=============================
	// オーバーライド関数
	//=============================
	virtual HRESULT Init() override;
	virtual void Uninit() override;
	virtual void Update(const float fDeltaTime, const float fDeltaRate, const float fSlowRate) override;
	virtual void Draw() override;

	//=============================
	// メンバ関数
	//=============================
	void Kill();	// 削除

	//=============================
	// パターン
	//=============================
	void ChangeMoveControl(CPlayerControlMove* control);		// 移動の操作変更
	void ChangeActionControl(CPlayerControlAction* control);	// アクションの操作変更

	//=============================
	// 静的関数
	//=============================
	/*
		@brief		生成処理
		@details	プレイヤーインデックス
		@details	チームサイド
	*/
	static CPlayerAI *Create(const int nIdx, const CGameManager::TeamSide team);

private:

	//=============================
	// メンバ関数
	//=============================
	//-----------------------------
	// その他関数
	//-----------------------------
	void Move(const float fDeltaTime, const float fDeltaRate, const float fSlowRate) override;	// 移動
	void DeleteControl() override;	// 操作削除

	//=============================
	// メンバ変数
	//=============================
	//-----------------------------
	// パターン用インスタンス
	//-----------------------------
	CPlayerControlMove* m_pControlMove;		// 移動操作
	CPlayerControlAction* m_pControlAction;	// アクション操作
};

#endif
