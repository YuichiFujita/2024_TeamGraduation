//==========================================================================
// 初期化処理
//==========================================================================
HRESULT CRanking::Init()
{

	// 初期化処理
	if (FAILED(CScene::Init()))
	{// 失敗した場合
		return E_FAIL;
	}

	// ファイル読み込み
	Load();

	// ソート
	Sort();

	// ファイル保存
	Save();

	// マップ生成
	CreateMap();

	CSound::GetInstance()->PlaySound(CSound::LABEL_BGM_RANKING);

	// 成功
	return S_OK;
}

