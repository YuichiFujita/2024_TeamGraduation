#include "number_2D.h"
#include "manager.h"
#include "renderer.h"

//==========================================================================
// コンストラクタ
//==========================================================================
CNumber2D::CNumber2D(int nPriority, const LAYER layer) : CNumber(nPriority, layer)
{
	// 値のクリア
	m_pObj2D = nullptr;			// オブジェクト2Dのオブジェクト
}

//==========================================================================
// デストラクタ
//==========================================================================
CNumber2D::~CNumber2D()
{

}

//==========================================================================
// 初期化処理
//==========================================================================
HRESULT CNumber2D::Init()
{
	// 生成処理
	m_pObj2D = CObject2D_Anim::Create(GetPosition(), 10, 1, 1.0f, false, GetPriority());
	
	// 自動再生停止
	m_pObj2D->SetEnableAutoPlay(false);

	// 初期化処理
	CNumber::Init();
	return S_OK;
}

//==========================================================================
// 終了処理
//==========================================================================
void CNumber2D::Uninit()
{
	// 終了処理
	CNumber::Uninit();
}

//==========================================================================
// 削除処理
//==========================================================================
void CNumber2D::Kill()
{
	// 削除処理
	if (m_pObj2D != nullptr)
	{
		m_pObj2D->Kill();
		m_pObj2D = nullptr;
	}

	// 削除処理
	CNumber::Kill();
}

//==========================================================================
// 更新処理
//==========================================================================
void CNumber2D::Update(const float fDeltaTime, const float fDeltaRate, const float fSlowRate)
{
	// 頂点情報設定処理
	CNumber2D::SetVtx();
}

//==========================================================================
// 描画処理
//==========================================================================
void CNumber2D::Draw()
{
	m_pObj2D->Draw();
}

//==========================================================================
// 頂点情報設定処理
//==========================================================================
void CNumber2D::SetVtx()
{
	m_pObj2D->SetVtx();
}

//==========================================================================
// 種類設定
//==========================================================================
void CNumber2D::SetType(const TYPE type)
{
	// 種類設定
	CObject::SetType(type);
	m_pObj2D->CObject::SetType(type);
}
