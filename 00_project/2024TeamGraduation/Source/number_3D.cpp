#include "number_3D.h"
#include "manager.h"
#include "renderer.h"

//==========================================================================
// コンストラクタ
//==========================================================================
CNumber3D::CNumber3D(int nPriority, const LAYER layer) : CNumber(nPriority, layer)
{
	// 値のクリア
	m_pObj3D = nullptr;			// オブジェクト3Dのオブジェクト
}

//==========================================================================
// デストラクタ
//==========================================================================
CNumber3D::~CNumber3D()
{

}

//==========================================================================
// 初期化処理
//==========================================================================
HRESULT CNumber3D::Init()
{
	
	// 生成処理
	m_pObj3D = CObject3DAnim::Create(MyLib::PosGrid2(10, 1), GetPosition(), 1.0f, false, GetPriority());
	
	// 自動再生停止
	m_pObj3D->SetEnableStop(true);

	// 初期化処理
	CNumber::Init();
	return S_OK;
}

//==========================================================================
// 終了処理
//==========================================================================
void CNumber3D::Uninit()
{
	// 終了処理
	CNumber::Uninit();
}

//==========================================================================
// 削除処理
//==========================================================================
void CNumber3D::Kill()
{
	// 削除処理
	if (m_pObj3D != nullptr)
	{
		m_pObj3D->Kill();
		m_pObj3D = nullptr;
	}

	// 削除処理
	CNumber::Kill();
}

//==========================================================================
// 更新処理
//==========================================================================
void CNumber3D::Update(const float fDeltaTime, const float fDeltaRate, const float fSlowRate)
{
	// 頂点情報設定処理
	CNumber3D::SetVtx();
}

//==========================================================================
// 描画処理
//==========================================================================
void CNumber3D::Draw()
{
	
}

//==========================================================================
// 頂点情報設定処理
//==========================================================================
void CNumber3D::SetVtx()
{
	m_pObj3D->SetVtx();
}

//==========================================================================
// 種類設定
//==========================================================================
void CNumber3D::SetType(const TYPE type)
{
	// 種類設定
	CObject::SetType(type);
	m_pObj3D->CObject::SetType(type);
}