//==========================================================================
bool CPlayerPosAdjInLeft::IsLineOut(CPlayer* pPlayer)
{
	// ラインを越えている場合フラグONを返す
	if (pPlayer->GetPosition().x > CPlayerPosAdjIn::COMEBACK_LINE) { return true; }

	// ラインを越えていない場合フラグOFFを返す
	return false;
}
