//==========================================================================
// 生成処理
//==========================================================================
CNumber* CNumber::Create(CMultiNumber::EObjType type, int nPriority)
{
	// メモリの確保
	CNumber* pNumber = nullptr;

	switch (type)
	{
	case CMultiNumber::OBJTYPE_NONE:
		break;

	case CMultiNumber::OBJTYPE_2D:
		break;

	case CMultiNumber::OBJTYPE_3D:
		break;

	case CMultiNumber::OBJTYPE_BILLBOARD:
		break;

	default:
		assert(false);
		break;
	}

	if (pNumber != nullptr)
	{
		// クラスの初期化
		if (FAILED(pNumber->Init()))
		{ // 初期化に失敗した場合

			// クラスの終了
			SAFE_UNINIT(pNumber);
			return nullptr;
		}
	}

	return pNumber;
}

